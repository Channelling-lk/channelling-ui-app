package lk.channelling.ui.service;

import lk.channelling.ui.entity.User;
import lk.channelling.ui.repository.UserRepository;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.security.core.GrantedAuthority;
import org.springframework.security.core.authority.SimpleGrantedAuthority;
import org.springframework.security.core.userdetails.UserDetails;
import org.springframework.security.core.userdetails.UserDetailsService;
import org.springframework.security.core.userdetails.UsernameNotFoundException;
import org.springframework.stereotype.Service;

import java.util.Optional;
import java.util.Set;
import java.util.stream.Collectors;


@Service
public class UserDetail implements UserDetailsService {

    UserRepository userRepository;

    @Autowired
    public void setUserRepository(UserRepository userRepository) {
        this.userRepository = userRepository;
    }

    @Override
    public UserDetails loadUserByUsername(String username) throws UsernameNotFoundException {
        Optional<User> user = userRepository.findByUsernameOrEmail(username, username);

        if (user.isEmpty())
            throw new UsernameNotFoundException("Incorrect user name or password");

        Set<GrantedAuthority> authorities = user.get().getRoles().stream().map(role -> new SimpleGrantedAuthority((role.getName()))).collect(Collectors.toSet());
        return new org.springframework.security.core.userdetails.User(username, user.get().getPassword(), authorities);

    }
}
